# Operators

# Arithmetic operators are used with numeric values to perform common mathematical operations:

paper = 12
scissors = 5

# + 	Addition 	x + y 	
print("Addition", paper + scissors)

# - 	Subtraction 	x - y 	
print("Substraction", paper - scissors)

# * 	Multiplication 	x * y 	
print("Multiplication", paper * scissors)

# / 	Division 	x / y 	
print("Division", paper / scissors)

#// 	Floor division 	x // y
print("Floor division", paper // scissors)

# % 	Modulus 	x % y 	
print("Modulus", paper % scissors)

# ** 	Exponentiation 	x ** y 	
print("Exponentiation", paper ** scissors)
